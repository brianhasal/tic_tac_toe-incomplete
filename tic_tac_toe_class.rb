class BoardPiece
  def initialize(box_number, states_array)
    @box_number = box_number
    @states_array = states_array
    @current_state = "neutral"
  end

  def box_number
    @box_number
  end

  def states_array
    @states_array
  end


  def current_state
    @current_state
  end


  def current_state=(input)
    @current_state = input
  end

  def display
    if @current_state == "oplayer"
      return states_array[1]
    elsif @current_state == "xplayer"
      return states_array[2]
    else 
      return states_array[0]
    end 
  end

